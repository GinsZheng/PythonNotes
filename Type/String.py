# -- coding ➤  utf-8 --

name = 'ada lovelace'
# 首字母大写
print('title ➤ ', name.title())

# 全大写
print('upper ➤ ', name.upper())

# 全小写
print('lower ➤ ', name.lower())

# 删除空白(trim)
blankSpace = '  python  '
print('strip ➤ ', blankSpace.strip())
print('rstrip ➤ ', blankSpace.rstrip())
print('lstrip ➤ ', blankSpace.lstrip())

